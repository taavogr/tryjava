    private void calcularPromedio() {
        double prom=0;
        switch (cboTipo.getSelectedIndex()){
            case 1:
                prom=Matematica.Promedio(Integer.parseInt(txtParcial.getText()),Integer.parseInt(txtFinal.getText()));
                break;
            case 2 :
                prom=Matematica.Promedio(Integer.parseInt(txtParcial.getText()),Integer.parseInt(txtFinal.getText()),Integer.parseInt(txtPractica.getText()));
                break;
            case 3:
                prom=Matematica.Promedio(Integer.parseInt(txtParcial.getText()),Integer.parseInt(txtFinal.getText()),Integer.parseInt(txtPractica.getText()),Integer.parseInt(txtLaboratorio.getText()));
                break;
            case 4:
                prom=Matematica.Promedio(Integer.parseInt(txtParcial.getText()),Integer.parseInt(txtFinal.getText()),Integer.parseInt(txtPractica.getText()),Integer.parseInt(txtLaboratorio.getText()),Integer.parseInt(txtProyecto.getText()));
                break;
                
            case 0:
                JOptionPane.showMessageDialog(null, "Seleccionat tipo de Calificacion");
                break;
                
        }
        
        txtPromedio.setText(prom+"");
        if (prom>=14) {
            lblEstado.setText("Aprobado!");
            
        }else if (prom>=10 && prom<=14){
            lblEstado.setText("Sustitutorio!");
        }else{
           lblEstado.setText("Desaprobado!");
        }
    }
